export function normalizeText(value = '') {
  return value
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .trim()
}

export function makeReservationKey(reservationName, guestName) {
  return `${normalizeText(reservationName)}::${normalizeText(guestName)}`
}
